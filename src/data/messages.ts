export const addTask = (item: any) => {
  const _messages = [...messages];
  _messages.push({ ...item });
  messages = [..._messages];
};

export const onUpdate = (newItem: any) => {
  const _messages = [...messages];
  const oldItem = _messages.filter((msg) => (msg.id === newItem.id))[0] ?? {}
  const idx = _messages.indexOf(oldItem)
  if (idx > -1) {
    _messages.splice(idx, 1, { ...newItem });
  }
  messages = [..._messages];
}

export const onDelete = (item: any) => {
  const _messages = [...messages];
  const oldItem = _messages.filter((msg) => (msg.id === item.id))[0] ?? {}
  const idx = _messages.indexOf(oldItem)
  if (idx > -1) {
    _messages.splice(idx, 1);
  }
  messages = [..._messages];
}

